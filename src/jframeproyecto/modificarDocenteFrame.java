    private void bt_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_buscarActionPerformed

        String dniM = dniMod.getText();

        listaDocentes obj = new listaDocentes();
        pos = obj.BuscarXDni(dniM);
        if (pos != -1) {
            nombreDocente.setText(listaDocentes.arrListDocente.get(pos).getNombre());
            apellidosDocente.setText(listaDocentes.arrListDocente.get(pos).getApellidos());
            dniDocente.setText(listaDocentes.arrListDocente.get(pos).getDNI());
            aulaDocente.setText(listaDocentes.arrListDocente.get(pos).getAula());
            claveDocente.setText(listaDocentes.arrListDocente.get(pos).getClave());

            if ("Femenino".equals(listaDocentes.arrListDocente.get(pos).getSexo())) {
                bt_femenino.setSelected(true);
            } else if ("Masculino".equals(listaDocentes.arrListDocente.get(pos).getSexo())) {
                bt_masculino.setSelected(true);
            }
        } else {
            JOptionPane.showMessageDialog(null, "DNI no encontrado");
            grupo_Sexo.clearSelection();
            dniDocente.setText("");
            nombreDocente.setText("");
            apellidosDocente.setText("");
            aulaDocente.setText("");
            claveDocente.setText("");
            System.out.println(listaDocentes.arrListDocente);
        }
    }//GEN-LAST:event_bt_buscarActionPerformed

