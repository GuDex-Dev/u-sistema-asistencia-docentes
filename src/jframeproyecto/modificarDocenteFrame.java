    private void bt_modActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_modActionPerformed

        boolean okey = validarCampos(dniDocente.getText(), nombreDocente.getText(), apellidosDocente.getText(), claveDocente.getText(), aulaDocente.getText());

        if (okey) {

            String dni = dniDocente.getText();
            String nombre = nombreDocente.getText();
            String apellidos = apellidosDocente.getText();
            String sexo = "";
            if (bt_femenino.isSelected()) {
                sexo = "Femenino";
            } else if (bt_masculino.isSelected()) {
                sexo = "Masculino";
            } else {
                sexo = "No se seleciono";
            }

            String aula = aulaDocente.getText();
            String clave = claveDocente.getText();

            if (dni.length() == 8 && clave.length() == 4) {
                if ((listaDocentes.BuscarXDni(dni) == -1 || listaDocentes.BuscarXDni(dni) == pos) && (listaDocentes.BuscarXClave(clave) == -1 || listaDocentes.BuscarXClave(clave) == pos)) {

                    listaDocentes.arrListDocente.set(pos, new docente(dni, nombre, apellidos, sexo, aula, clave, listaDocentes.arrListDocente.get(pos).getHoraEntrada(), listaDocentes.arrListDocente.get(pos).getEstadoEntrada(), listaDocentes.arrListDocente.get(pos).getHoraSalida(), listaDocentes.arrListDocente.get(pos).getEstadoSalida()));

                    JOptionPane.showMessageDialog(null, "Modificaci√≥n Exitosa");
                    grupo_Sexo.clearSelection();
                    dniDocente.setText("");
                    nombreDocente.setText("");
                    apellidosDocente.setText("");
                    aulaDocente.setText("");
                    claveDocente.setText("");
                    System.out.println(listaDocentes.arrListDocente);

                } else {
                    JOptionPane.showMessageDialog(null, "DNI o Clave ya existe");
                }
            } else {
                if (dni.length() < 8 && clave.length() < 4) {
                    JOptionPane.showMessageDialog(null, "Error" + "\n" + "DNI de 8 caracteres y Clave de 4 caracteres");
                    dniDocente.setText("");
                    claveDocente.setText("");
                } else if (dni.length() < 8) {
                    JOptionPane.showMessageDialog(null, "El DNI requiere de 8 caracteres");
                    dniDocente.setText("");
                } else {
                    JOptionPane.showMessageDialog(null, "La clave requiere de 4 caracteres");
                    claveDocente.setText("");
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Rellene todos los campos");
        }
    }//GEN-LAST:event_bt_modActionPerformed

    public static boolean validarCampos(String nombre, String apellido, String dni, String clave, String aula) {
        boolean okey = false;

        if (nombre.length() > 0 && apellido.length() > 0 && dni.length() > 0 && clave.length() > 0 && aula.length() > 0) {
            okey = true;
        }
        return okey;
    }

